class Profile {
  final int id;
  final String name;
  final String title;
  final String email;
  final String avatarUrl;
  final String companyName;
  final String teamName;

  Profile({
    required this.id,
    required this.name,
    required this.title,
    required this.email,
    required this.avatarUrl,
    required this.companyName,
    required this.teamName,
  });
}
